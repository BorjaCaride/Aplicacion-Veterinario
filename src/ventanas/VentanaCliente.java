    private void btnAltaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltaClienteActionPerformed

        String dni = entDniCli.getText().toString();
        String nombre = entNombreCli.getText().toString();
        String apellido = entApellidoCli.getText().toString();
        String direccion = entDireccionCli.getText().toString();
        String telefono = entTelefonoCli.getText();
        String fecha = entFechaCli.getText();
        String correo = entCorreoCli.getText();
        int t;

        if (!telefono.equalsIgnoreCase("")) {
            t = Integer.parseInt(telefono);
        } else {
            t = 0;
        }
        if ((correo.equalsIgnoreCase("")) | (dni.equalsIgnoreCase("")) | (nombre.equalsIgnoreCase("")) | (apellido.equalsIgnoreCase("")) | (direccion.equalsIgnoreCase("")) | (telefono.equalsIgnoreCase("")) | (fecha.equalsIgnoreCase(""))) {

            lblmensaje.setText("No se han introducido todos los datos");
        } else {
            SimpleDateFormat formato = new SimpleDateFormat("dd-MM-yyyy");
            Date fechaDate = null;
            try {
                fechaDate = formato.parse(fecha);
            } catch (ParseException ex) {
                System.out.println(ex);
            }
            System.out.println(fechaDate);
            Altas.altaCliente(fechaDate, t, direccion, correo, dni, nombre, apellido);

            lblmensaje.setText("Cliente aÃ±adido");
            entDniCli.setText("");
            entNombreCli.setText("");
            entApellidoCli.setText("");
            entFechaCli.setText("");
            entDireccionCli.setText("");
            entTelefonoCli.setText("");
            entCorreoCli.setText("");

            String[] fila = {dni, nombre, apellido, fecha, telefono, direccion, correo};

            modelo.addRow(fila);

        }


    }//GEN-LAST:event_btnAltaClienteActionPerformed

    private void btnBajaCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaCliActionPerformed

        String dni = entDniCli.getText();

        Bajas.bajaCliente(dni);

        int fila = modelo.getRowCount();
        for (int i = 0; i < fila; i++) {
            modelo.removeRow(0);

        }
        Cargar.cargaClientes(modelo);

        entDniCli.setText("");
        entNombreCli.setText("");
        entFechaCli.setText("");
        entDireccionCli.setText("");
        entApellidoCli.setText("");
        entTelefonoCli.setText("");
        entCorreoCli.setText("");

        lblmensaje.setText("Se ha eliminado el Cliente\ncon DNI: " + dni);

    }//GEN-LAST:event_btnBajaCliActionPerformed

