    private void tablaAnimalesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaAnimalesMouseClicked
        // TODO add your handling code here:
        int seleccion = tablaAnimales.rowAtPoint(evt.getPoint());

        entREIAC.setText(String.valueOf(tablaAnimales.getValueAt(seleccion, 0)));
        entNombreAni.setText(String.valueOf(tablaAnimales.getValueAt(seleccion, 1)));
        entTipoAni.setText(String.valueOf(tablaAnimales.getValueAt(seleccion, 2)));
        entAlimentacionAni.setText(String.valueOf(tablaAnimales.getValueAt(seleccion, 3)));
        ComboBox.setSelectedItem(String.valueOf(tablaAnimales.getValueAt(seleccion, 4)));
    }//GEN-LAST:event_tablaAnimalesMouseClicked

