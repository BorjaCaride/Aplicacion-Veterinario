    private void btnAltaAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltaAnimalActionPerformed

        String reiac = entREIAC.getText().toString();
        String nombre = entNombreAni.getText().toString();
        String tipo = entTipoAni.getText().toString();
        String alimentacion = entAlimentacionAni.getText().toString();
        String dni = (String) ComboBox.getSelectedItem();
        System.out.println(dni);

        if ((reiac.equalsIgnoreCase("") | (nombre.equalsIgnoreCase("") | (tipo.equalsIgnoreCase("") | (dni.equalsIgnoreCase("")) | (alimentacion.equalsIgnoreCase("")))))) {

            lblmensaje.setText("No se han introducido todos los datos");
        } else {
            Altas.altaAnimal(reiac, nombre, tipo, alimentacion, dni);
            lblmensaje.setText("Animal añadido");
            entREIAC.setText("");
            entNombreAni.setText("");
            entTipoAni.setText("");
            entAlimentacionAni.setText("");

            String[] fila = {reiac, nombre, tipo, alimentacion, dni};

            modelo.addRow(fila);

        }


    }//GEN-LAST:event_btnAltaAnimalActionPerformed

    private void btnBajaAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaAnimalActionPerformed

        String reiac = entREIAC.getText();

        Bajas.bajaAnimal(reiac);

        int fila = modelo.getRowCount();
        for (int i = 0; i < fila; i++) {
            modelo.removeRow(0);

        }
        Cargar.cargaTablaAnimales(modelo);

        entNombreAni.setText("");
        entREIAC.setText("");
        entTipoAni.setText("");
        lblmensaje.setText("Se ha eliminado el animnal\ncon REIAC: " + reiac);

    }//GEN-LAST:event_btnBajaAnimalActionPerformed

